a = int(input("zadaj číslo a:")) # input je prikazom vstupu, vypise na obrazovku text v zatvorke a to co uzivatel napise na klavesnici, ulozi do premennej a 
b = int(input ("zadaj číslo b:")) # input vracia vzdy vysledok vo formate retazca (postupnost znakov resp. text), funkcia int() pretypuje retazec na cele cislo
sucet = a + b
print ("súčet je:", sucet)
sucin = a * b
print ("súčin je:", sucin)
rozdiel = a - b
print ("rozdiel je:", rozdiel)
podiel = a / b
print ("podiel je:", podiel)
